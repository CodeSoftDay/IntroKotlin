fun main(args: Array<String>){

    // Arreglos
    val country = arrayOf("Nicaragua", "Costa Rica", "Salvador", "Panama", "Guatemala", "Honduras")

    // Mostramos un elemento del arreglo
    println(country[1])

    // Con un for recoremos todo el array e lo mostramos en consola
    for (i in country){
        println(i)
    }

}